#ifndef HGL_DATATYPE_TYPEDEF_INCLUDE
#define HGL_DATATYPE_TYPEDEF_INCLUDE

namespace hgl
{
	typedef unsigned char       uchar;  ///< 无符号字符型
	typedef unsigned int        uint;   ///< 无符号整型
	typedef unsigned long		ulong;	///< 无符号长整型

	typedef   signed __int8      int8;  ///<有符号 8位整型
	typedef unsigned __int8     uint8;  ///<无符号 8位整型
	typedef   signed __int16     int16; ///<有符号16位整型
	typedef unsigned __int16    uint16; ///<无符号16位整型
	typedef   signed __int32     int32; ///<有符号32位整型
	typedef unsigned __int32    uint32; ///<无符号32位整型
	typedef   signed __int64     int64; ///<有符号64位整型
	typedef unsigned __int64    uint64; ///<无符号64位整型
};

#endif//HGL_DATATYPE_TYPEDEF_INCLUDE
